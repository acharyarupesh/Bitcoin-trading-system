    public void refreshTable(){
        DefaultTableModel dtm = (DefaultTableModel) productsTable.getModel();
        dtm.setRowCount(0);
        for(Product p: productCatalog.getProductCatalog())
        {
            Object row[]= new Object[5];
            row[0]=p;
            row[1]=p.getID();
            row[2]=p.getPrice();
            row[3]=p.getQuantity();
            row[4]=p.getProductCategory();
            dtm.addRow(row);
        }
        productsTable.getTableHeader().setFont(new Font("Tahoma", Font.BOLD, 16));
    }
