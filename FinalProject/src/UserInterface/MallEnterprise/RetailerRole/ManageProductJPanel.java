    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow=productsTable.getSelectedRow();
        if(selectedRow>=0){
           int dialogResult=JOptionPane.showConfirmDialog(this, "Are you sure you want to remove this?", "Warning",JOptionPane.YES_NO_OPTION);
           if(dialogResult==JOptionPane.YES_OPTION)
           {
               Product p=(Product)productsTable.getValueAt(selectedRow, 0);
               productCatalog.removeProduct(p);
               JOptionPane.showMessageDialog(this, "Product deleted", "SUCCESS",JOptionPane.INFORMATION_MESSAGE);
           }
           refreshTable();
           }
         else
        {
            JOptionPane.showMessageDialog(this, "Please select a row first", "ERROR", JOptionPane.ERROR_MESSAGE);
        } 
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        int row = productsTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
        Product p = (Product) productsTable.getValueAt(row, 0);
        UpdateProductJPanel upjpanel=new UpdateProductJPanel(userProcessContainer, p);
        userProcessContainer.add("upjpanel",upjpanel);
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

