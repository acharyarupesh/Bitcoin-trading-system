    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:

        if (productNameTextField.getText().trim().isEmpty() || quantityTextField.getText().trim().isEmpty() || priceTextField.getText().trim().isEmpty() || jComboBox1.getSelectedItem().toString().equals("--Select--")) {
            JOptionPane.showMessageDialog(null, "All fields are mandatory", "Info", JOptionPane.INFORMATION_MESSAGE);
        } else if (!quantityTextField.getText().matches("\\d+") || !priceTextField.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(null, "Please enter valid quantity or price");
            priceTextField.setText("");
            quantityTextField.setText("");
        }else{
            Product p = productCatalog.addProduct();
            p.setProductName(productNameTextField.getText());
            p.setQuantity(Integer.parseInt(quantityTextField.getText()));
            p.setPrice(Integer.parseInt(priceTextField.getText()));
            p.setProductCategory(jComboBox1.getSelectedItem().toString());
            JOptionPane.showMessageDialog(null, "Product added!", "Info", JOptionPane.INFORMATION_MESSAGE);
            productNameTextField.setText("");
            priceTextField.setText("");
            quantityTextField.setText("");
            jComboBox1.setSelectedIndex(0);
        }
        
    }//GEN-LAST:event_saveButtonActionPerformed


