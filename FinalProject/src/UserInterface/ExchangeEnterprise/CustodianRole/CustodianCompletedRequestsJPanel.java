    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) completedRequestsJTable.getModel();
        dtm.setRowCount(0);
        
        for (WorkRequest wr : userAccount.getWorkqueue().getWorkRequestList()) {
            System.out.println(wr.getClass());
            Object[] row = new Object[6];
            row[0] = wr.getSender();
            row[1] = wr.getRequestedDate();
            row[2] = wr.getMessage();
            row[3] = wr;
            row[4] = " ";
            row[5] = wr.getBitcoinsRequested();

            dtm.addRow(row);
        }
        completedRequestsJTable.getTableHeader().setFont(new Font("Tahoma", Font.BOLD, 16));
    }

